package hwronak;
/**
 * 8. Write a Java program to divide two numbers and print on the screen.
 * Test Data: 50/3
 * Expected Output: 16
 */
public class DivideTwoNumber {

    public static void main(String[] args) {
        //The two integers are v1 and v2
        int v1 = 50;
        int v2 = 3;
        int division = v1 / v2;
        //Concatenation
        System.out.println("The " + v1 + " is divided by " + v2 + " the answer is " + division);

    }

}
